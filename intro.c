// ============================================================
// --- NOUVEAU SYSTEME (v2) : tile IDs VRAM directs ---
// Encoding :
//   0x80        = herbe vide (TYPE_EMPTY)
//   0x67..0x75  = tiles de route directes
//   0xFF        = baraque 1x1 (TILE_BARAQUE_NW)
//   0xFE        = sentinel (continuation d'un batiment multi-tiles)
//   tile NW     = coin haut-gauche d'un batiment (ex: 0x87=maison 3x3)
// Batiments et tailles :
//   3x3 : 0x87 HOUSE, 0xA0 FARM, 0xC1 CHURCH, 0x59 MEDIA, 0x76 SAWMILL
//   4x4 : 0x25 MALL, 0x90 FACTORY, 0xAD WOOD, 0xCA HOSPITAL, 0xDA POWER, 0xEA MINE
//   2x2 : 0xA9 PLANT, 0xBD POLICE, 0x3D SCHOOL, 0x81 BAR, 0x63 BARAQUE
// ============================================================

static uint8_t intro_tile_size_v2(uint8_t t) {
    if (t == 0x87u || t == 0xA0u || t == 0xC1u || t == 0x59u || t == 0x76u) return 3u;
    if (t == 0x25u || t == 0x90u || t == 0xADu || t == 0xCAu ||
        t == 0xDAu || t == 0xEAu)                                             return 4u;
    if (t == 0xA9u || t == 0xBDu || t == 0x3Du || t == 0x81u ||
        t == 0x63u)                                                           return 2u;
    return 1u;
}

static uint8_t intro_is_bldg_nw(uint8_t t) {
    return (t != 0x80u && t != 0xFEu && t != 0xFFu &&
            !(t >= 0x67u && t <= 0x75u));
}

