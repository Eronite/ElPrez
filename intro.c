static void plot_building_intro_v2(uint8_t x, uint8_t y, uint8_t base_tile, uint8_t size) {
    for (uint8_t j = 0; j < size; j++) {
        for (uint8_t i = 0; i < size; i++) {
            uint16_t index = (uint16_t)(y + j) * 32u + (x + i);
            if (index < 1024u) {
                ram_map[index] = base_tile + (i + (j * size));
            }
        }
    }
}

void generate_intro_map() {
    uint16_t i;
    for (i = 0u; i < 1024u; i++) ram_map[i] = TYPE_EMPTY;

    for (i = 0u; i < 1024u; i++) {
        uint8_t t = intro_building_layout_v2[i];
        if (t == 0x80u) continue;

        uint8_t x = (uint8_t)(i % 32u);
        uint8_t y = (uint8_t)(i / 32u);

        if (t >= 0x67u && t <= 0x75u) {
            ram_map[i] = t;
        } else if (t == 0xFEu || t == 0xFFu) {
            ram_map[i] = t;
        } else if (intro_is_bldg_nw(t)) {
            uint8_t sz = intro_tile_size_v2(t);
            if (x + sz <= 32u && y + sz <= 32u) {
                plot_building_intro_v2(x, y, t, sz);
            }
        }
    }
}




void display_logo_sprites(){
    uint8_t sp_idx = 0;
    
    // --- Configuration "EL" (Bloc 2x2) ---
    // Centrage horizontal : (160px - 16px) / 2 = 72
    // On ajoute +8 pour l'offset de l'écran Game Boy (X commence à 8)
    uint8_t el_x = 72 + 8; 
    uint8_t el_y = 50 + 16; // Position haute (Y commence à 16)
    
    // On suppose que les tuiles de "EL" commencent à l'index 50 dans la VRAM
    for (uint8_t row = 0; row < 2; row++) {
        for (uint8_t col = 0; col < 2; col++) {
            set_sprite_tile(sp_idx, 50 + (row * 2) + col);
            move_sprite(sp_idx, el_x + (col * 8), el_y + (row * 8));
            sp_idx++;
        }
    }

    // --- Configuration "PRESIDENTE" (Bloc 2x9) ---
    // Centrage horizontal : (160px - 72px) / 2 = 44
    // +8 offset = 52
    uint8_t pres_x = 44 + 8;
    uint8_t pres_y = el_y + 20; // Placé 20 pixels sous "EL" (16px de haut + 4px d'espace)

    // On suppose que les tuiles de "PRESIDENTE" suivent immédiatement (index 54)
    for (uint8_t row = 0; row < 2; row++) {
        for (uint8_t col = 0; col < 9; col++) {
            set_sprite_tile(sp_idx, 54 + (row * 9) + col);
            move_sprite(sp_idx, pres_x + (col * 8), pres_y + (row * 8));
            sp_idx++;
        }
    }
}


void intro_title_animation(void) {
    uint8_t x_scroll = 0;
    uint8_t y_scroll = 0;
    uint16_t timer = 0;

    DISPLAY_OFF;
    HIDE_WIN;

    // Les assets titleEl/titlePresidente sont chargés par main.c AVANT d'appeler cette fonction
    // (données en bank 1, inaccessibles depuis bank 2)


// Génère la ville à partir du tableau
    generate_intro_map();

    // Envoie tout au background
    set_bkg_tiles(0, 0, 32, 32, ram_map);


    // AFFICHAGE DU TITRE (Sprites) ---
    display_logo_sprites();

    SHOW_BKG;
    SHOW_SPRITES;
    DISPLAY_ON;

    // --- 4. BOUCLE DE SCROLLING ET ANIMATION ---
    while(1) {
        timer++;

        // Scrolling automatique fluide
        x_scroll++; 
        if (timer % 2 == 0) y_scroll++; // Scroll Y deux fois plus lent

        SCX_REG = x_scroll;
        SCY_REG = y_scroll;

        // Effet WOW : Le titre ondule légèrement (vague)
// Note : On déplace les 22 sprites utilisés (0 à 21)
        if (timer % 32 == 0) {
            int8_t wave = (timer % 64 < 32) ? 1 : -1;
            for (uint8_t i = 0; i < 22; i++) {
                scroll_sprite(i, 0, wave);
            }
        }

        if (joypad() & J_START) break;
        wait_vbl_done();
    }


    // --- NETTOYAGE POST-INTRO ---

// 1. On cache TOUS les sprites matériels (40 au total sur GB)
for(uint8_t i = 0; i < 40; i++) {
    move_sprite(i, 0, 0);
}

// cursor_data rechargé par main.c après retour (données en bank 1)

// 3. On remet le registre de scroll à zéro pour le menu
SCX_REG = 0;
SCY_REG = 0;

    //for(uint8_t i=0; i<22; i++) move_sprite(i, 0, 0);

}


