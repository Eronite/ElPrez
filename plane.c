// Séquence : A(30) -> B(30) -> A(30) -> C(30) -> pause(180) -> recommence
// water_anim_frame : 0=A, 1=B, 2=A, 3=C, 4=pause
static uint8_t  water_anim_phase = 0;
static uint16_t water_anim_timer = 0;
#define WATER_ANIM_DUR_FRAME  80u
#define WATER_ANIM_DUR_PAUSE  4000u

void water_anim_update(void) {
    if (is_menu_open) return;

    water_anim_timer++;

    // phase 6 = pause longue
    uint16_t dur = (water_anim_phase == 6u) ? WATER_ANIM_DUR_PAUSE : WATER_ANIM_DUR_FRAME;

    if (water_anim_timer >= dur) {
        water_anim_timer = 0;
        water_anim_phase++;
        if (water_anim_phase == 1u) {
            set_bkg_data(0x6Du, 5u, water_anim_B);
            set_bkg_data(0x76u, 3u, water_anim_B2);
        } else if (water_anim_phase == 2u) {
            set_bkg_data(0x6Du, 5u, water_anim_C);
            set_bkg_data(0x76u, 3u, water_anim_C2);
        } else if (water_anim_phase == 3u) {
            set_bkg_data(0x6Du, 5u, water_anim_D);
            set_bkg_data(0x76u, 3u, water_anim_D2);
        } else if (water_anim_phase == 4u) {
            set_bkg_data(0x6Du, 5u, water_anim_C);
            set_bkg_data(0x76u, 3u, water_anim_C2);
        } else if (water_anim_phase == 5u) {
            set_bkg_data(0x6Du, 5u, water_anim_B);
            set_bkg_data(0x76u, 3u, water_anim_B2);
        } /*else if (water_anim_phase == 6u) {
            // entrée en pause : remettre A
            set_bkg_data(0x6Du, 5u, water_anim_A);
            set_bkg_data(0x76u, 3u, water_anim_A2);
        }*/ else {
            // fin de pause : retour phase 0
            water_anim_phase = 0;
        }
    }
}
