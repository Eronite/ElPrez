// --- Positions X de départ pour chaque type ---
// plane1 (diag haut-droite) : part du bas-gauche hors map
//   coordonnées monde en pixels ; map = 64*8 = 512px
//   on part de x hors gauche, y hors bas
static const int16_t plane1_start_x[3] = { -16, -48, 48 };  // en pixels monde
static const int16_t plane1_start_y[3] = { 530, 530, 530 }; // sous la map

// plane2 (vers le bas) : part du haut hors map, 3 positions X
static const int16_t plane2_start_x[3] = { 80, 24, 160 };  // en pixels monde
static const int16_t plane2_start_y[3] = { -16, -16, -16 }; // au-dessus

// --- Vitesse ---
#define PLANE_PX_STEP  3u   // avance d'1px toutes les N frames

// --- État ---
#define PLANE_PAUSE_FRAMES  600u  // 10s à 60fps

static int16_t  plane_px;
static int16_t  plane_py;
static uint8_t  plane_type;       // 0 = plane1 (diag), 1 = plane2 (bas)
static uint8_t  plane_x_idx;      // 0-2 : indice de la position X courante
static uint8_t  plane_active;     // 1 = en vol, 0 = en pause
static uint16_t plane_pause;      // compteur de pause
static uint8_t  plane_px_timer;

// Charge les tiles du type courant en VRAM sprite (appelé depuis main.c via wrapper)
void plane_load_tiles(void) {
    const unsigned char *src = (plane_type == 0) ? plane1 : plane2;
    set_sprite_data(PLANE_TILE_0, 4u, src);
}

static void plane_spawn(void) {
    if (plane_type == 0) {
        plane_px = plane1_start_x[plane_x_idx];
        plane_py = plane1_start_y[plane_x_idx];
    } else {
        plane_px = plane2_start_x[plane_x_idx];
        plane_py = plane2_start_y[plane_x_idx];
    }
    plane_load_tiles();
    plane_active  = 1;
    plane_px_timer = 0;
}

void plane_init(void) {
    plane_type    = 0;
    plane_x_idx   = 0;
    plane_active  = 0;
    plane_pause   = PLANE_PAUSE_FRAMES;
    plane_px_timer = 0;
}

void plane_update(int16_t cam_x, int16_t cam_y) {
    if (is_menu_open) {
        move_sprite(PLANE_SPR_BASE,   0u, 0u);
        move_sprite(PLANE_SPR_BASE+1, 0u, 0u);
        move_sprite(PLANE_SPR_BASE+2, 0u, 0u);
        move_sprite(PLANE_SPR_BASE+3, 0u, 0u);
        return;
    }

    if (!plane_active) {
        if (plane_pause > 0) {
            plane_pause--;
        } else {
            plane_spawn();
        }
        move_sprite(PLANE_SPR_BASE,   0u, 0u);
        move_sprite(PLANE_SPR_BASE+1, 0u, 0u);
        move_sprite(PLANE_SPR_BASE+2, 0u, 0u);
        move_sprite(PLANE_SPR_BASE+3, 0u, 0u);
        return;
    }

    // Avancer d'1px toutes les PLANE_PX_STEP frames
    plane_px_timer++;
    if (plane_px_timer >= PLANE_PX_STEP) {
        plane_px_timer = 0;
        if (plane_type == 0) {
            // Diagonal haut-droite : +1px en x, -1px en y
            plane_px++;
            plane_py--;
        } else {
            // Vers le bas : +1px en y seulement
            plane_py++;
        }
    }

    // Détecter sortie de la map (512x512 px + marge)
    uint8_t out_of_bounds = 0;
    if (plane_type == 0) {
        // sorti par le haut ou la droite
        if (plane_py < -16 || plane_px > 528) out_of_bounds = 1;
    } else {
        // sorti par le bas
        if (plane_py > 528) out_of_bounds = 1;
    }

    if (out_of_bounds) {
        plane_active = 0;
        plane_pause  = PLANE_PAUSE_FRAMES;
        // Avancer vers le prochain type et la prochaine position X
        plane_x_idx++;
        if (plane_x_idx >= 3u) {
            plane_x_idx = 0;
            plane_type  = (plane_type == 0) ? 1u : 0u;
        }
        move_sprite(PLANE_SPR_BASE,   0u, 0u);
        move_sprite(PLANE_SPR_BASE+1, 0u, 0u);
        move_sprite(PLANE_SPR_BASE+2, 0u, 0u);
        move_sprite(PLANE_SPR_BASE+3, 0u, 0u);
        return;
    }

    // Calcul position écran
    int16_t sx  = plane_px - cam_x + 8;
    int16_t sy  = plane_py - cam_y + 16;
    int16_t sx1 = sx + 8;
    int16_t sy2 = sy + 8;

    // Clip : si hors écran visible ou au-dessus du HUD, cacher
    uint8_t hud_oam_y = (current_tool < 17u || current_tool == TOOL_ONETILEHOME) ? 144u : 152u;
    if (sx < -8 || sx1 > 176 || sy < 0 || sy2 >= (int16_t)hud_oam_y) {
        move_sprite(PLANE_SPR_BASE,   0u, 0u);
        move_sprite(PLANE_SPR_BASE+1, 0u, 0u);
        move_sprite(PLANE_SPR_BASE+2, 0u, 0u);
        move_sprite(PLANE_SPR_BASE+3, 0u, 0u);
    } else {
        set_sprite_tile(PLANE_SPR_BASE,   PLANE_TILE_0);
        move_sprite(PLANE_SPR_BASE,   (uint8_t)sx,  (uint8_t)sy);
        set_sprite_tile(PLANE_SPR_BASE+1, PLANE_TILE_1);
        move_sprite(PLANE_SPR_BASE+1, (uint8_t)sx1, (uint8_t)sy);
        set_sprite_tile(PLANE_SPR_BASE+2, PLANE_TILE_2);
        move_sprite(PLANE_SPR_BASE+2, (uint8_t)sx,  (uint8_t)sy2);
        set_sprite_tile(PLANE_SPR_BASE+3, PLANE_TILE_3);
        move_sprite(PLANE_SPR_BASE+3, (uint8_t)sx1, (uint8_t)sy2);
    }
}

